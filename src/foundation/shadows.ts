/**
 * Flume shadow tokens.
 */
export interface Shadows {
  /**
   * Very small, very subtle shadow.
   */
  xsmall: string

  /**
   * Small, subtle shadow.
   */
  small: string

  /**
   * Medium-sized shadow.
   */
  medium: string

  /**
   * Large shadow used for depth.
   */
  large: string

  /**
   * Shadow for focused elements.
   */
  focus: string
}
