/**
 * Flume breakpoints.
 */
export interface Breakpoints {
  /**
   * String index signature for type compat.
   */
  [key: string]: string

  /**
   * Smallest screen size.
   */
  default: string

  /**
   * Extra small screen size.
   */
  xs: string

  /**
   * Small screen size.
   */
  sm: string

  /**
   * Medium screen size.
   */
  md: string
  /**
   * Large screen size.
   */
  lg: string
  /**
   * Large screen size.
   */
  xl: string
  /**
   * Extra Large screen size.
   */
  xxl: string
}
